public class Practice_4_comparisonOperation {
    public static void main(String[] args) {
        int val1 = 1 ;
        int val2 = 2 ;
        
        if(val1 == val2){
            System.out.println("val1 == val2"); // Will not print
        }

        if(val1 != val2){
            System.out.println("val1 != val2"); // Will print
        }

        if(val1 >= val2){
            System.out.println("val1 >= val2"); // Will not print
        }

        if(val1 <= val2){
            System.out.println("val1 <= val2"); // Will print
        }

        if(val1 > val2){
            System.out.println("val1 > val2"); // Will not print
        }

        if(val1 < val2){
            System.out.println("val1 < val2"); // Will print
        } 
    }
}
